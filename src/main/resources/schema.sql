    CONSTRAINT uq_earn_cancel_tx UNIQUE (transaction_id),
    CONSTRAINT uq_earn_cancel_earn UNIQUE (earn_id),
    CONSTRAINT fk_earn_cancel_tx FOREIGN KEY (transaction_id) REFERENCES point_transaction(id),
    CONSTRAINT fk_earn_cancel_account FOREIGN KEY (account_id) REFERENCES point_account(id),
    CONSTRAINT fk_earn_cancel_earn FOREIGN KEY (earn_id) REFERENCES point_earn(id),
    CONSTRAINT chk_earn_cancel_amount CHECK (cancel_amount > 0)
);

CREATE TABLE point_usage (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    transaction_id BIGINT NOT NULL,
    account_id BIGINT NOT NULL,
    order_no VARCHAR(100) NOT NULL,
    usage_amount DECIMAL(15, 0) NOT NULL,
    cancelled_amount DECIMAL(15, 0) NOT NULL DEFAULT 0,
    status VARCHAR(30) NOT NULL,
    created_at TIMESTAMP NOT NULL,
    updated_at TIMESTAMP NOT NULL,

    CONSTRAINT uq_point_usage_tx UNIQUE (transaction_id),
    CONSTRAINT uq_point_usage_order UNIQUE (account_id, order_no),
    CONSTRAINT fk_point_usage_tx FOREIGN KEY (transaction_id) REFERENCES point_transaction(id),
    CONSTRAINT fk_point_usage_account FOREIGN KEY (account_id) REFERENCES point_account(id),
    CONSTRAINT chk_point_usage_status CHECK (status IN ('USED', 'PARTIAL_CANCELED', 'CANCELED')),
    CONSTRAINT chk_point_usage_amount CHECK (
        usage_amount > 0
        AND cancelled_amount >= 0
        AND cancelled_amount <= usage_amount
    )
);

CREATE TABLE point_usage_allocation (
