CREATE TABLE point_ledger (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    transaction_id BIGINT NOT NULL,
    account_id BIGINT NOT NULL,
    earn_id BIGINT,
    usage_id BIGINT,
    usage_cancel_id BIGINT,
    usage_allocation_id BIGINT,
    ledger_type VARCHAR(50) NOT NULL,
    delta_amount DECIMAL(15, 0) NOT NULL,
    account_balance_before DECIMAL(15, 0) NOT NULL,
    account_balance_after DECIMAL(15, 0) NOT NULL,
    earn_available_before DECIMAL(15, 0),
    earn_available_after DECIMAL(15, 0),
    description VARCHAR(500),
    created_at TIMESTAMP NOT NULL,

    CONSTRAINT fk_ledger_tx FOREIGN KEY (transaction_id) REFERENCES point_transaction(id),
    CONSTRAINT fk_ledger_account FOREIGN KEY (account_id) REFERENCES point_account(id),
    CONSTRAINT fk_ledger_earn FOREIGN KEY (earn_id) REFERENCES point_earn(id),
    CONSTRAINT fk_ledger_usage FOREIGN KEY (usage_id) REFERENCES point_usage(id),
    CONSTRAINT fk_ledger_usage_cancel FOREIGN KEY (usage_cancel_id) REFERENCES point_usage_cancel(id),
    CONSTRAINT fk_ledger_usage_alloc FOREIGN KEY (usage_allocation_id) REFERENCES point_usage_allocation(id),
    CONSTRAINT chk_ledger_type CHECK (ledger_type IN (
        'EARN_INCREASE',
        'EARN_CANCEL_DECREASE',
        'USE_DECREASE',
        'USE_CANCEL_ORIGINAL_INCREASE',
        'USE_CANCEL_NEW_EARN_INCREASE',
        'EXPIRE_DECREASE'
    )),
    CONSTRAINT chk_ledger_delta CHECK (delta_amount <> 0),
    CONSTRAINT chk_ledger_account_balance CHECK (
        account_balance_before >= 0
        AND account_balance_after >= 0
        AND account_balance_after = account_balance_before + delta_amount
    ),
    CONSTRAINT chk_ledger_earn_available_before CHECK (
        earn_available_before IS NULL OR earn_available_before >= 0
    ),
    CONSTRAINT chk_ledger_earn_available_after CHECK (
        earn_available_after IS NULL OR earn_available_after >= 0
    )
);

